                                <p>
                                <h4>POURQUOI LA NOUVELLE NAISSANCE ?</h4>
                                Dieu a créé l’homme et lui a donné la domination sur la terre (Ps 8). Il l’a créé à Son
                                image et selon
                                Sa ressemblance parce qu’Il voulait que l’homme soit à Son niveau, capable de marcher et
                                de parler avec
                                Lui dans une fraiche communion (Ge 1 : 26 – 28 ; 3 : 8). Il avait donné à l’homme le
                                genre de vie la plus
                                élevée – être Sa famille sur la terre. Il a donné à l’homme l’instruction de soumettre
                                la terre et de la
                                remplir aussi avec des êtres à sa propre image (de l’homme) qui est celle de Dieu
                                Lui-même. Et, Dieu
                                n’avait donné qu’une seule exigence à l’homme (Adam) (Ge. 2 : 16 – 17) : l’obéissance.
                                Mais, par sa désobéissance à cette seule instruction, l’homme, par Adam, s’est fait
                                lui-même sujet et moins
                                puissant que l’ennemi de Dieu, Satan. La communion intime, la communication et la
                                relation dont il jouissait
                                avec Dieu étaient brisées. Satan est devenu son seigneur et, l’homme a été immédiatement
                                séparé de Dieu
                                (la source de sa vie spirituelle) et est mort spirituellement comme Dieu le lui avait
                                annoncé. Satan est
                                un esprit mort. La séparation avec Dieu (le péché) est une mort spirituelle (Ge. 3:8-11
                                ; Ep. 2:1-3; Ge. 2:17; Rom. 6:23). <br>
                                C’est quoi la mort spirituelle? Cette sentence de mort ne parle pas seulement de votre
                                courte vie sur cette
                                terre et qui finit avec la mort physique et la tombe.
                                <br>
                                Il n’y a pas de temps durant lequel vous n’existez pas car votre esprit existera
                                éternellement au Ciel ou en enfer.
                                Par conséquent, la <b>“mort spirituelle”</b> ne signifie pas cesser d’exister mais être
                                coupé de Dieu, être séparé de Sa communion; c à d. avoir la nature qui vous fait pécher.
                                Cette nature déchue n’est pas seulement incapable de demeurer dans la Présence de Dieu,
                                elle ne peut pas non plus rester en harmonie avec le reste de la création de Dieu, ni
                                maintenant ni dans l’avenir.
                                Tout être humain né d’Adam a hérité de la nature de péché (Rom 5 : 12). <br>
                                </p>
                                <p>
                                    A cause de cette désobéissance, l’avenir du monde a été affecté – toute la semence
                                    d’Adam naissait dans
                                    ce monde avec la nature de péché au lieu de la justice, - avec la nature de Satan au
                                    lieu de la nature de Dieu.
                                    Quelque chose devait être faite : la rédemption menant à la nouvelle naissance. <br>
                                    Cela pouvait prendre beaucoup de temps à Dieu pour pourvoir à la voie servant à
                                    ramener encore l’homme
                                    dans l’union et l’entente avec Lui. Mais, Dieu devait le faire.
                                <h4>CE QU’EST REELLEMENT LA NOUVELLE NAISSANCE</h4>
                                <blockquote>
                                    Ez. 11 : 19 – 20 « Je leur donnerai un même cœur, Et je mettrai en vous un esprit
                                    nouveau ; J’ôterai de leur corps
                                    le cœur de pierre, Et je leur donnerai un cœur de chair, 20 Afin qu’ils suivent mes
                                    ordonnances, Et qu’ils observent
                                    et pratiquent mes lois ; Et ils seront mon peuple, et je serai leur Dieu.
                                </blockquote>
                                <blockquote>
                                    2.Cor 5:17 « Si quelqu'un est en Christ, il est une nouvelle créature. Les choses
                                    anciennes sont passées; voici, toutes choses sont devenues nouvelles ».
                                </blockquote>
                                PVV « Si quelqu’un entre en communion vivante avec le Christ, il devient un homme
                                nouveau, il est recréé. L’ancien état est dépassé. Ce qu’il était autrefois a disparu.
                                La nouvelle création a déjà commencé ; voici : tout est devenu nouveau. »
                                <br>

                                <blockquote>Gal 6:15 « Car ce n'est rien que d'être circoncis ou incirconcis; ce qui est
                                    quelque chose, c'est d'être une nouvelle créature. »</blockquote>
                                </p>
                                <p>
                                    La nouvelle naissance est une nécessité pour être sauvé. Par elle, nous entrons en
                                    bonne relation avec Dieu. Elle est nécessaire avant qu’on ne puisse réclamer les
                                    promesses de Dieu.
                                    La nouvelle naissance n’est pas : la confirmation, le fait d’appartenir ou d’être
                                    membre d’une église, être baptisé dans l’eau, prendre les sacrements, observer les
                                    rites religieux, une réception intellectuelle de la Christianité, l’orthodoxie de la
                                    foi, aller à l’église, prier, lire la Bible, être moral, être cultivé ou raffiné,
                                    faire de bonnes œuvres, donner le meilleur de soi-même ou faire toutes sortes de
                                    bonnes choses que certains croient qu’elles peuvent les sauver.
                                    Le brigand sur la croix était sauvé sans ces choses. Il a tout simplement fait la
                                    seule chose nécessaire qu’il fallait : il a accepté Jésus comme son Sauveur
                                    personnel en se repentant et en se tournant vers Dieu de tout son cœur, comme un
                                    petit enfant.
                                    Quand Jésus dit à Nicodème : <b>« … il vous faut naître de nouveau » </b>, ce
                                    dernier pensant naturellement, demanda : <b>« … comment un homme peut-il naître
                                        quand il est vieux ? Peut-il rentrer dans le ventre de sa mère et naître ? ».
                                    </b> Jésus lui expliqua : <b>« ce qui est né de la chair est chair, et ce qui est né
                                        de l’Esprit est esprit » </b>(Jean 3 : 7, 4, 6).
                                    La nouvelle naissance est :
                                    La renaissance (naître de nouveau), la régénération de l’esprit humain.
                                    Une nouvelle création venant d’en haut – l’opération directe de la Parole de Dieu et
                                    de l’Esprit de Dieu sur votre vie (Jean 3 : 5 – 6) changeant votre esprit
                                    complètement quand vous vous repentez vraiment et vous tournez vers Dieu.
                                </p>
                                <b>Remarques : </b> <br>
                                <p> 1°). La nouvelle naissance n’a pu être possible qu’après la mort de Jésus à la croix
                                    car c’était le prix à payer par Dieu. Elle est aussi appelée : - naître de nouveau,
                                    naître d’eau et d’Esprit (Jean 3 : 5), naître de l’Esprit, régénération par la
                                    Parole de Dieu (1 Pierre 1 : 22 – 23), naître de Dieu.
                                    2°). La nature réelle de l’homme (Ge. 1 : 26, 27).

                                    L’homme a été créé à l’image de Dieu. On peut alors dire que l’homme est la copie de
                                    Dieu. Ce qu’est Dieu, c’est ce que l’homme est c à d :

                                </p>
                                <h5>a). Trois personnes en une par nature. (1 John 5:7). </h5>
                                <p>
                                    Dieu est un être trinitaire (trois personnes en une). Il a créé l’homme à Son image,
                                    l’homme doit aussi être trinitaire et cela est confirmé dans 1 Thes.5:23
                                    <br>
                                </p>
                                <h5>b). Un être spirituel. </h5>
                                <p> Dieu est ESPRIT (Jn.4:24). Donc, l’homme est essentiellement UN ETRE SPIRITUEL. Et,
                                    puisque ce qui est né de l’esprit est esprit (Jn.3 : 6), tout celui qui est né de
                                    Dieu est esprit selon l’ordre de Dieu son Père céleste qui lui a donné la vie (Jean
                                    1:12-13).
                                    <br>
                                </p>
                                <b>Qui est l’homme?</b> <br>
                                <p>
                                    L’HOMME EST UN ESPRIT, IL POSSEDE UNE AME ET HABITE DANS UN CORPS (1 Thess. 5 : 23).
                                    Ceci constitue la nature trinitaire de l’homme. Les trois parties de l’homme sont
                                    distinctes dans leur fonction, mais elles sont en interactions.<br>
                                    Par mon corps j’entre en contact avec la sphère physique (très évident)
                                    Par mon esprit, j’entre en contact avec le domaine spirituel
                                    Par mon âme, j’entre en contact avec le domaine intellectuel (1 Cor. 14 : 14) <br>

                                </p>


                                <b>L’esprit de l’homme</b> <br>
                                <p>
                                    L’esprit de l’homme est le vrai homme, l’homme réel. C’est la part de l’homme qui
                                    contient la vie de Dieu, la vie spirituelle (Genesis 2:7).
                                    Sans l’esprit, ni l’âme ni le corps ne peuvent fonctionner.
                                    L’esprit s’exprime lui-même à travers l’âme et le corps.
                                    C’est avec l’esprit que l’homme entre en contact avec Dieu (1 Cor. 2:14).<br>

                                    L’esprit REGENÉRÉ est parfois appelé :<br>
                                    <b>L’homme intérieur - 2 Cor. 4:16, Job 32:8</b> <br>
                                    <b>L’homme caché du cœur - 1 Pierre 3:4</b> <br>
                                    <b>L’homme nouveau - Eph. 4:24, Col. 3:10 </b>
                                </p>

                                <b>L’âme : </b>
                                <p>
                                    L’âme comprend la pensée (intelligence), les émotions et la volonté.
                                </p>
                                <b>La pensée (intelligence): </b>
                                <p>
                                    La pensée est l’intellect de l’homme. La pensée (intelligence) de l’homme est la
                                    valve centrale du contrôle de l’être humain.
                                    L’esprit ne peut pas avoir de relation avec le corps sans l’approbation de la pensée
                                    de même que le corps ne peut avoir de
                                    relation avec l’esprit sans l’approbation de la pensée.
                                </p>
                                <br>
                                <b>Les émotions : </b>
                                <p>
                                    C’est le domaine des sentiments – la colère, la peur, l’amertume, la joie, la
                                    tristesse, etc.
                                </p>

                                <b>La volonté :

                                </b>
                                <p>Dieu a donné à l’homme la volonté par laquelle il a les prérogatives (liberté)
                                    absolues de faire des choix (De. 30:19).
                                    iii).
                                </p>
                                <b>Le corps : </b>
                                <p>
                                    Le corps de l’homme est le lieu d’habitation et de son l’esprit et de son l’âme.
                                    Il est aussi considéré comme étant le Temple du Saint-Esprit (1 Cor. 3:16, 1 Cor.
                                    6:19).
                                    Le corps est la part matérielle de l’homme avec laquelle il entre en relation avec
                                    le monde physique
                                    autour de lui par le canal des CINQ SENS:
                                    La vue (les yeux) - L’odorat (le nez) - Le toucher (les mains) Le goût (la langue) -
                                    L’ouïe (les oreilles) <br>

                                    <br>
                                    Lorsque le corps meurt, il retourne dans la poussière (terre) d’où il a été pris
                                    (Ge. 3:19). Il sera changé en immortel à l’enlèvement
                                    (1 Thess. 4:16-17).
                                    L’esprit continue de vivre. Il est éternel, il ne peut jamais mourir et, l’homme
                                    véritable, créé à l’image de Dieu est esprit, il ne meurt
                                    donc pas. <br>
                                    Dans Ph. 1 :23, 24 et 2 Co. 4 : 16, l’apôtre Paul nous montre qu’il y a un homme
                                    intérieur et un homme extérieur. L’homme extérieur (corps)
                                    n’est pas
                                    le vrai homme, c’est seulement son habitation. L’homme intérieur est le vrai homme,
                                    il ne vieillit jamais, il se renouvelle chaque jour.
                                    Dans Jean 3 :3 -6, Jésus parlait à Nicodème non de la naissance physique mais de la
                                    naissance spirituelle et, la part de l’homme qui est
                                    nait de nouveau, qui est sauvé, qui devient une nouvelle créature en Christ ou qui
                                    reçoit la vie de Dieu, la vie éternelle c’est l’esprit.
                                    2 Co. 5 : 17 parle aussi de l’homme intérieur. <br> Quand on naît de nouveau, on ne
                                    reçoit pas un nouveau corps. L’homme extérieur ne
                                    change pas. Dieu n’en fait rien, c’est nous qui devons faire quelque chose.<br>
                                    Nous devons apprendre à contrôler la chair ainsi qu’à soumettre notre corps à
                                    l’esprit (Rom. 12 : 1 ; 1 Co. 9 :27).<br>
                                    L’âme quant à elle, doit être renouvelée par la Parole (Rom. 12 : 2) car son salut
                                    est un processus. Elle doit aussi être restaurée
                                    (Jacques. 1 : 21 ; Ps. 23 : 3).
                                    <br>
                                </p>
                                <h4>LE PRIX DE LA REDEMPTION MENANT A LA NOUVELLE NAISSANCE </h4>

                                <p> Dans le Jardin d’Eden, Dieu savait quel prix Il devait payer afin de récupérer Sa
                                    famille (Ge. 3 : 15). Il était disposé à sacrifier Son
                                    Fils unique pour ramener l’homme en communion avec Lui (Jean 3 : 16). C’était le
                                    prix à payer pour rendre la nouvelle naissance possible
                                    et, Dieu, l’a payé par amour pour toi et moi. </p>
                                <h5>1. Présentation du Rédempteur : le dernier Adam.</h5>
                                <p>
                                    Pour nous sauver, Dieu a envoyé Son Fils Jésus, appelé « le dernier Adam » (1 Cor 15
                                    : 45 – 47) dont la mission était la même que celle
                                    du premier – créer une race des gens que Dieu pouvait encore appelés Sa Famille.
                                    C’est ce qu’on appelle la nouvelle naissance – devenir
                                    un fils de Dieu et, par conséquent, partager Sa nature (2 Pierre 1 : 3 – 4).
                                    Jésus n’a pas été conçu par Adam ; il est né de Dieu. Par conséquent, la lignée
                                    entre Jésus et Adam a été rompue et, pour la
                                    première fois depuis la chute d’Adam, un homme est né libre du péché.
                                    Adam, le premier homme que Dieu a créé, était engendré par Dieu. De même, Jésus, le
                                    dernier Adam, était aussi né directement de Dieu
                                    ; Il a été conçu du Saint-Esprit (Luc 1 : 26 – 38) et possédait, par conséquent, la
                                    même nature parfaite que le premier homme Adam
                                    avait avant sa séparation avec Dieu.
                                    La catastrophe de la race humaine est venue quand Adam a perdu sa nature libre de
                                    tout péché par sa désobéissance. Et, toute la race
                                    humaine a hérité la peine de sa transgression.
                                    C’est pourquoi Jésus pouvait devenir notre péché. Noter, qu’Il n’est pas devenu
                                    PÉCHEUR, Il est devenu PÉCHÉ ; il y a une différence
                                    (2 Cor. 5 : 21). Il a pris la place de l’homme dans le jugement afin que les
                                    exigences, les revendications légales contre l’humanité
                                    soient satisfaites et que les hauts courts du Ciel puissent une fois encore régner
                                    en faveur de la race humaine.
                                    Jésus a été présenté à l’homme comme son Rédempteur quand la Parole est devenue
                                    chair et a vécu parmi nous (Jean 1 : 14, 29 – 34) :
                                    He. 10:5 C'est pourquoi Christ, entrant dans le monde, dit: Tu n'as voulu ni
                                    sacrifice ni offrande, Mais tu m'as formé un corps;
                                    He. 10:6 Tu n'as agréé ni holocaustes ni sacrifices pour le péché. <br>
                                </p>
                                <h5>2. Le prix de la rédemption.</h5>

                                <p> Matthieu 26 : 37 – 46 : trente-trois ans après, le Fils de Dieu mourut agonisant sur
                                    la Croix et accomplit ainsi les exigences que
                                    Dieu Lui avait données. A la croix, Il a payé à Dieu la dette de l’humanité.
                                    <b>1°). La souffrance de Jésus (He. 4 : 15-16 ; 1 Pierre 3 : 18 ; Es. 53).</b><br>
                                    Jésus le Parfait, a souffert la punition à notre place. Quoiqu’étant absolument sans
                                    tâches, Jésus a pris notre place à la Croix.
                                    Contrairement aux sacrifices de la Loi qui étaient continuels et répétés, le
                                    sacrifice totalement suffisant de Christ se fit une
                                    fois pour toutes. (He. 7 : 26 – 27).
                                    <br>

                                    <b>2°). La mort de Jésus. </b> <br>

                                    Le Sauveur a donné Sa vie et est mort à notre place pour obtenir notre salut (He. 2
                                    : 14, 17 ; App. 1 : 5 ; Jean 3 : 16).
                                    Dieu a fourni le paiement du prix du péché en sacrifiant Son propre Fils qui a payé
                                    avec Son propre Sang. Jésus-Christ a payé
                                    le prix de notre péché et a brisé le pouvoir du péché dans les vies de ceux qui
                                    s’identifient eux-mêmes avec Son œuvre sur la Croix.
                                    <br>
                                    <b>En résume : Jésus a pris notre place.
                                        Il est mort à votre place (Rom. 5:8-9)
                                        Il est allé en enfer à votre place (Ps.16:10, Col. 2:15).
                                        Il a versé Son sang pour vous (He. 9:12).</b> <br>
                                </p>
                                <h5> 3.. Les trois éléments principaux de la rédemption</h5>


                                <p> Le rachat ou la rédemption l’homme a trois éléments que Dieu voulait accomplir :
                                    1°). La rémission des péchés (Matthieu 26 : 28 ; Actes 2 : 38).
                                    Rémission (pardon) veut dire littéralement dans ces passages que Dieu « éloigne de
                                    nous » nos péchés. Il a pris nos péchés et les a envoyés à la croix où tous les
                                    péchés ont été pris en compte. A travers Jésus, la rémission des péchés a été
                                    pourvue et, l’homme qui était séparé de Dieu par le péché, peut alors maintenant
                                    revenir à Lui.
                                    2°). La restauration, récupération de la ressemblance à Dieu (2 Co. 3 :17-18).
                                    Dieu, cependant, cherche non seulement à pardonner les péchés de l’homme mais à
                                    l’aider à retrouver Sa ressemblance, à restaurer la part intérieure de Son caractère
                                    afin que la bonté de Christ devienne évidente dans nos vies. C’est le Saint-Esprit
                                    en tant que participant au plan du salut qui vient, par Son ministère, restaurer
                                    l’image du Seigneur en nous.
                                    3°). La restauration de l’état (position) de domination et d’autorité (Luc 12 : 32 ;
                                    Matthieu 11 : 12).
                                    En plus de la rémission des péchés et de la restauration du caractère de Dieu en
                                    l’homme, Dieu veut réinstaller l’homme dans la position qu’il était censé occupée à
                                    la création c à d. une position de domination et d’autorité dotée de l’amour de Dieu
                                    (Ge. 1 : 26 ; 9 : 1, 2 ; Ps 8 : 3 – 9).

                                </p>

